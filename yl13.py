# Sisestan lemmiklooma
animal = input("Sisesta oma lemmikloom: ")
print(animal)

# Prindi lemmiklooma esimene täht
print(animal[0])

# Teen loomade listi
mylist = ["hamster", "madu", "hobune"]
print(mylist)

# Lisan lemmiklooma ka loomade listi
mylist.append(animal)
print(mylist)

# Sätin listi tähestikulisele järjekorda
mylist.sort()
print(mylist)

# Prindin sisestatud lemmiklooma viimase tähe
print(mylist[-1][-1])

# kaks viimast tähte
print(mylist[-1][-2:])

